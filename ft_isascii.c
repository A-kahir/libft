#include "libft.h"

int ft_isascii(int ch)
{
    if (ch >= 0 && ch <= 127)
    {
        return (1);
    }
    else
    {
        return (0);
    }
}

int main()
{
    int c = 48;
    int res = ft_isascii(c);
    printf("%d\n", res);
}